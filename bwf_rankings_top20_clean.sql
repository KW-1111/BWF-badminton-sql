-- BWF Top 20 Rankings (MS, MD, WS, WD, XD) as of July 2025

DROP TABLE IF EXISTS bwf_rankings;
CREATE TABLE bwf_rankings (
  category ENUM('MS','MD','WS','WD','XD') NOT NULL,
  world_ranking INT NOT NULL,
  player1 VARCHAR(100) NOT NULL,
  age1 INT,
  nationality1 CHAR(3) NOT NULL,
  player2 VARCHAR(100),
  age2 INT,
  nationality2 CHAR(3),
  points INT NOT NULL,
  PRIMARY KEY (category, world_ranking)
);

DROP TABLE IF EXISTS player_info;
CREATE TABLE player_info (
  category ENUM('MS','MD','WS','WD','XD') NOT NULL,
  world_ranking INT NOT NULL,
  player1 VARCHAR(100) NOT NULL,
  player2 VARCHAR(100),
  playing_hand1 ENUM('Right', 'Left') NOT NULL,
  playing_hand2 ENUM('Right', 'Left'),
  earning_usd INT NOT NULL,
  career_win INT NOT NULL,
  career_lose INT NOT NULL
);

